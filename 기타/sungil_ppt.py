def rect(slide, l, t, w, h, fill=None, line=None, lw=0.5):
    from pptx.util import Emu
    s = slide.shapes.add_shape(1, l, t, w, h)
    s.line.fill.background()
    if fill:
        s.fill.solid(); s.fill.fore_color.rgb = fill
    else:
        s.fill.background()
    if line:
        s.line.color.rgb = line; s.line.width = Pt(lw)
    else:
        s.line.fill.background()
    return s


def tb(slide, l, t, w, h, text, fs=10, bold=False, color=DKGRAY,
       align=PP_ALIGN.LEFT, wrap=True, italic=False):
    box = slide.shapes.add_textbox(l, t, w, h)
    tf  = box.text_frame
    tf.word_wrap = wrap
    p   = tf.paragraphs[0]
    p.alignment = align
    run = p.add_run()
    run.text = text
    run.font.size  = Pt(fs)
    run.font.bold  = bold
    run.font.italic = italic
    run.font.color.rgb = color
    run.font.name  = FONT_KR
    return box


def header(slide, title, subtitle="(주)성일엔텍 컨설팅보고서"):
    rect(slide, Cm(0), Cm(0), SLIDE_W, Cm(1.75), fill=NAVY)
    rect(slide, Cm(0), Cm(1.75), SLIDE_W, Cm(0.08), fill=ACCENT)
    tb(slide, Cm(0.5), Cm(0.2), Cm(27), Cm(1.4),
       title, fs=15, bold=True, color=WHITE)
    tb(slide, Cm(27.5), Cm(0.45), Cm(6), Cm(0.9),
       subtitle, fs=8, color=MGRAY, align=PP_ALIGN.RIGHT)


def footer(slide, n):
    rect(slide, Cm(0), Cm(18.55), SLIDE_W, Cm(0.5), fill=LGRAY)
